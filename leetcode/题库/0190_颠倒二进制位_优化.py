# 分治方法，翻转32位，可以分别翻转前16与后16位，再翻转两部分，
# 以此类推，翻转8、4、2位

m0 = 0x55555555 # 0b01010101010101010101010101010101
m1 = 0x33333333 # 0b00110011001100110011001100110011
m2 = 0x0f0f0f0f # 0b00001111000011110000111100001111
m3 = 0x00ff00ff # 0b00000000111111110000000011111111
m4 = 0x0000ffff # 0b00000000000000001111111111111111
class Solution:
    def reverseBits(self, n: int) -> int:
        n = n>>1&m0 | (n&m0)<<1 # 交换相邻位